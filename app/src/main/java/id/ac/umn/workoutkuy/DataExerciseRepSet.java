package id.ac.umn.workoutkuy;

public class DataExerciseRepSet {
    int reps;
    int sets;

    public DataExerciseRepSet(int reps, int sets) {
        this.reps = reps;
        this.sets = sets;
    }

    public int getReps() {
        return reps;
    }

    public void setReps(int reps) {
        this.reps = reps;
    }

    public int getSets() {
        return sets;
    }

    public void setSets(int sets) {
        this.sets = sets;
    }
}
